                            <h3>Survival Tips for During a Flooding Event</h3>
                            <ul>
                                <li>
                                    <p>Listen to radio, television, or Internet news reports for information of weather that could result in flooding.</p>
                                </li>
                                <li>
                                    <p>In the event of the warning of flash flooding, evacuate the area immediately and head for high ground.  Flash floods can develop and occur very rapidly, so waste no time.</p>
                                </li>
                                <li>
                                    <p>Should you be instructed to do so by emergency officials, turn off main utilities and valves.</p>
                                </li>

                                <li>
                                    <p>Do not attempt to operate or even touch electrical equipment that is in standing water.</p>
                                </li>
                                <li>
                                    <p>
                                        Avoid any contact with flood water.  The water may be contaminated with raw sewage, petrochemicals (e.g., oil, gasoline, diesel fuel, etc.), and harmful microbes.
                                        <ul>
                                            <li><p>Be mindful of children as they may attempt to play in flood waters.</p></li>
                                        </ul>
                                    </p>
                                </li>
                                <li>
                                    <p>Do not walk through moving water.  As little as six inches of moving water can make a person fall.  Two feet of rushing water can move a vehicle such as a car or light truck.</p>
                                </li>
                                <li>
                                    <p>If a mandatory evacuation order is issued and it is safe to go, do so without delay.</p>
                                </li>

                            </ul>   
                            <h3>Survival Tips for After a Flooding Event</h3>
                            <ul>
                                <li>
                                    <p>Only return home or to your place of business when authorities have stated it is safe to do so.</p>
                                </li>
                                <li>
                                    <p>If the power is still out, only use flashlights when entering your home or business as flammable material may be present.</p>
                                </li>
                                <li>
                                    <p>Continue to monitor the radio and other media sources for updates.</p>
                                </li>
                                <li>
                                    <p>Stay out of any structures still surrounded by flood waters.</p>
                                </li>
                                <li>
                                    <p>Note that damaged sewage and septic systems are very dangerous to human life.  They should be repaired as soon as possible.</p>
                                </li>
                                <li>
                                    <p>Stay clear of downed power lines and report any to the electrical company or cooperative.</p>
                                </li>
                                <li>
                                    <p>Thoroughly clean and disinfect any article that came in contact with flood water.  Discard articles that cannot be properly cleaned.</p>
                                </li>
                                <li>
                                    <p>Do not consume any food, beverage, or tobacco product that came in contact with flood waters as they are likely contaminated.If absolutely necessary, drinking water may be made safe by boiling and filtering.</p>
                                </li>
                            </ul>
