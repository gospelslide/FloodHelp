                            <h2>FAQ's</h2>
                            <hr class="star-primary">
<h4>How dangerous is flooding?</h4>
<p>Floods are a dangerous hazard throughout the world. On average in the United States, about 165 people are killed and about $2 billion of damage occurs each year. Most people underestimate the power and destructiveness of flood waters.</p>                            

<h4>Where can I learn more about flood forecasting?</h4>
<p>Most currently available flood maps are used to assist planners in identifying and preparing for flooding scenarios. These maps portray statistics based on long-term historical records to estimate and forecast an approaching weather system.</p>

<h4>Are there different types of flooding?</h4>
<p>There are two basic kinds of floods, flash floods and the more widespread river flooding. Flash floods generally cause greater loss of life and river floods generally cause greater loss of property.</p>

<h4>How are floods predicted?</h4>
<p>Several types of data can be collected to assist hydrologists predict when and where floods might occur:
    <li>
        <p>Monitoring amount of rainfall</p>
    </li>
    <li>
        <p>Monitoring rate of change in river levels</p>
    </li>
    <li>
        <p>Knowledge about type of storms</p>
    </li>
    <li>
        <p>Knowledge about the characteristics of a river's drainage basin, such as soil-moisture conditions, ground temperature,etc</p>
    </li>
</p>

